const hexToHsl = (hex: string): [number, number, number] => {
  hex = hex.replace(/^#/, '')
  const r = parseInt(hex.slice(0, 2), 16) / 255
  const g = parseInt(hex.slice(2, 4), 16) / 255
  const b = parseInt(hex.slice(4, 6), 16) / 255
  const max = Math.max(r, g, b),
    min = Math.min(r, g, b)
  let h = 0,
    s = 0
  const l = (max + min) / 2
  const d = max - min
  if (d !== 0) {
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min)
    switch (max) {
      case r:
        h = (g - b) / d + (g < b ? 6 : 0)
        break
      case g:
        h = (b - r) / d + 2
        break
      case b:
        h = (r - g) / d + 4
        break
    }
    h *= 60
  }
  return [h, s * 100, l * 100]
}

const hslToHex = (h: number, s: number, l: number): string => {
  s /= 100
  l /= 100
  const k = (n: number) => (n + h / 30) % 12
  const a = s * Math.min(l, 1 - l)
  const f = (n: number) => {
    const col = l - a * Math.max(-1, Math.min(k(n) - 3, Math.min(9 - k(n), 1)))
    return Math.round(255 * col)
      .toString(16)
      .padStart(2, '0')
  }
  return `#${f(0)}${f(8)}${f(4)}`
}

/** 生成调色板 */
export const generatePalette = (baseColor: string): Record<string, string> => {
  const [h, s, l] = hexToHsl(baseColor)
  const shades = [50, 100, 200, 300, 400, 500, 600, 700, 800, 900, 950]
  const palette: Record<string, string> = {}

  shades.forEach((shade) => {
    let newL = l
    let newS = s

    if (shade < 500) {
      // 亮色部分：提升亮度，轻微降低饱和度
      const t = (500 - shade) / 450
      newL = Math.min(98, l + t * (98 - l))
      newS = Math.max(30, s - t * (s - 30))
    } else if (shade > 500) {
      // 暗色部分：降低亮度，增加饱和度
      const t = (shade - 500) / 450
      newL = Math.max(8, l - t * (l - 8))
      newS = Math.min(100, s + t * (100 - s))
    }

    palette[shade] = hslToHex(h, newS, newL)
  })

  return palette
}
